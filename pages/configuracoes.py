def hash_password(password: str) -> str:
    """Cria hash da senha"""
    return hashlib.sha256(password.encode()).hexdigest()

def show_change_password_form():
    """Formul√°rio para alterar senha"""
    with st.form("change_password_form"):
        st.markdown("#### üîë Alterar Senha")
        
        current_password = st.text_input("Senha Atual", type="password", help="Digite sua senha atual")
        new_password = st.text_input("Nova Senha", type="password", help="M√≠nimo 6 caracteres")
        confirm_password = st.text_input("Confirmar Nova Senha", type="password", help="Digite a nova senha novamente")
        
        col_submit, col_cancel = st.columns(2)
        
        with col_submit:
            submitted = st.form_submit_button("üîë Alterar Senha", use_container_width=True, type="primary")
        
        with col_cancel:
            cancelled = st.form_submit_button("‚ùå Cancelar", use_container_width=True)
        
        if submitted:
            if not current_password or not new_password or not confirm_password:
                st.error("‚ö†Ô∏è Todos os campos s√£o obrigat√≥rios")
                return
            
            if new_password != confirm_password:
                st.error("‚ö†Ô∏è As senhas n√£o coincidem")
                return
            
            if len(new_password) < 6:
                st.error("‚ö†Ô∏è A senha deve ter pelo menos 6 caracteres")
                return
            
            # Verificar senha atual
            auth = get_auth()
            user = auth.get_current_user()
            
            if not user or 'id' not in user:
                st.error("‚ö†Ô∏è Erro ao identificar usu√°rio")
                return
            
            try:
                db = get_database()
                
                # Buscar hash atual da senha no banco
                result = db.execute_query(
                    "SELECT senha_hash FROM usuarios WHERE id = ?",
                    (user['id'],)
                )
                
                if not result:
                    st.error("‚ö†Ô∏è Usu√°rio n√£o encontrado")
                    return
                
                stored_hash = result[0]['senha_hash']
                
                # Verificar senha atual usando bcrypt
                if auth.verify_password(current_password, stored_hash):
                    # Senha atual est√° correta, criar hash da nova
                    new_hash = auth.hash_password(new_password)
                    
                    # Atualizar no banco
                    success = db.execute_update(
                        "UPDATE usuarios SET senha_hash = ? WHERE id = ?",
                        (new_hash, user['id'])
                    )
                    
                    if success:
                        st.success("‚úÖ Senha alterada com sucesso!")
                        st.info("üí° Use a nova senha no pr√≥ximo login")
                        
                        # Opcional: for√ßar logout para usar nova senha
                        if st.button("üö™ Fazer Logout Agora", key="logout_after_password_change"):
                            auth.logout_user()
                            st.rerun()
                    else:
                        st.error("‚ùå Erro ao alterar senha no banco de dados")
                else:
                    st.error("‚ö†Ô∏è Senha atual incorreta")
                    
            except Exception as e:
                st.error(f"‚ùå Erro ao alterar senha: {e}")
        
        if cancelled:
            st.rerun()

def show_activity_log():
    """Mostra log de atividades do usu√°rio"""
    auth = get_auth()
    user = auth.get_current_user()
    
    if user:
        db = DatabaseConnection()
        
        # Buscar registros de auditoria do usu√°rio
        logs = db.execute_query("""
            SELECT timestamp as data_hora, action as acao, details as detalhes 
            FROM logs_sistema 
            WHERE user_id = ? 
            ORDER BY timestamp DESC 
            LIMIT 20
        """, (user['usuario'],))
        
        st.markdown("#### üìã √öltimas Atividades")
        
        if logs:
            import pandas as pd
            df = pd.DataFrame(logs)
            df['data_hora'] = pd.to_datetime(df['data_hora'])
            df['data_hora'] = df['data_hora'].dt.strftime('%d/%m/%Y %H:%M:%S')
            
            # Usar fun√ß√£o segura para exibir DataFrame
            from utils.dataframe_utils import safe_dataframe
            safe_dataframe(
                df,
                column_config={
                    "data_hora": "Data/Hora",
                    "acao": "A√ß√£o",
                    "detalhes": "Detalhes"
                },
                use_container_width=True,
                hide_index=True
            )
        else:
            st.info("üìù Nenhuma atividade registrada")
    else:
        st.error("‚ö†Ô∏è Erro ao identificar usu√°rio")

